    public void dataLoad(){
        int r = table6.getSelectedRow();
        String id = table6.getValueAt(r, 0).toString();
        String name = table6.getValueAt(r, 1).toString();
        String gender = table6.getValueAt(r, 2).toString();
        String epf_num = table6.getValueAt(r, 3).toString();
        String emp_design = table6.getValueAt(r, 4).toString();
        String emp_dep = table6.getValueAt(r, 5).toString();
        
        id_Box.setText(id);
        emp_Name_Box.setText(name);
        if(gender.equals("male")){
            male_Btn.setSelected(true);
        }else if(gender.equals("female")){
            female_Btn.setSelected(true);
        }else{
            male_Btn.setSelected(false);
            female_Btn.setSelected(false);
        }
        emp_D_Box.setText(emp_design);
        emp_Dep_Box.setText(emp_dep);
        emp_Epf_Box.setText(epf_num);
        
    }
    
