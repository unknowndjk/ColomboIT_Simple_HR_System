    private void update_Emp_BtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_update_Emp_BtnActionPerformed
        int id = Integer.parseInt(id_Box.getText());
        String name = emp_Name_Box.getText();
        String gender;
        if(male_Btn.isSelected()){
            gender = "male";
        }else if(female_Btn.isSelected()){
            gender = "female";
        }else{
            gender = null;
        }
        int epf_number = Integer.parseInt(emp_Epf_Box.getText());
        String emp_dep = emp_Dep_Box.getText();
        String emp_design = emp_D_Box.getText();

        HRM_T up = new HRM_T();
        up.update(id, name, gender, epf_number, emp_design, emp_dep, table6);
    }//GEN-LAST:event_update_Emp_BtnActionPerformed

    private void add_Emp_BtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_add_Emp_BtnActionPerformed
        String name = emp_Name_Box.getText();
        String gender;
        if(male_Btn.isSelected()){
            gender = "male";
        }else if(female_Btn.isSelected()){
            gender = "female";
        }else{
            gender = null;
        }
        int epf_number;
        if(emp_Epf_Box.getText().equals("")){
            epf_number = 0;
        }else{
            epf_number = Integer.parseInt(emp_Epf_Box.getText());
        }
        String emp_dep = emp_Dep_Box.getText();
        String emp_design = emp_D_Box.getText();

        HRM_T add = new HRM_T();
        add.addEmployee(name, gender, epf_number, emp_design, emp_dep,table6);
        cleardata();
        dataload.empTableLoad(table6);

    }//GEN-LAST:event_add_Emp_BtnActionPerformed

